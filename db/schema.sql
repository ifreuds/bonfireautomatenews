-- ============================================================================
-- Bonfire Newsletter — database schema (Supabase / Postgres)
-- ============================================================================
-- Run this ONCE on a fresh Supabase project (SQL Editor, or `supabase db`).
-- It recreates every table, security function, RLS policy and the image bucket.
-- It contains NO secrets — you generate your own API tokens at the bottom.
--
-- Mental model:
--   * The AI worker only ever calls the SECURITY DEFINER functions below,
--     gated by a token. It can read its brief-archive and SUBMIT a draft.
--     It CANNOT publish. Only a human (admin token) can move a draft to
--     'published'. That is the entire approval gate.
--   * The public (anon key) can read ONLY published issues (RLS). Drafts are
--     invisible. api_tokens is unreadable (RLS on, zero policies).
-- ============================================================================

-- ---------------------------------------------------------------------------
-- 1. TABLES
-- ---------------------------------------------------------------------------

create table if not exists public.issues (
  id              uuid primary key default gen_random_uuid(),
  issue_date      date not null,
  newsletter      text not null default 'cloud',   -- 'cloud' | 'games'
  title           text,
  status          text not null default 'draft'
                    check (status in ('draft','pending','approved','published','rejected')),
  content_json    jsonb,        -- structured issue (English). The app renders this.
  content_json_cn jsonb,        -- structured issue (Chinese). Powers the /cn page.
  content_html    text,         -- legacy: some old issues stored raw HTML instead.
  submitted_by    text,
  approved_by     text,
  approved_at     timestamptz,
  published_at    timestamptz,
  review_note     text,
  created_at      timestamptz not null default now()
);

-- Dedup memory: every story URL ever published, so the AI never repeats one.
create table if not exists public.story_archive (
  id           uuid primary key default gen_random_uuid(),
  url          text unique not null,
  headline     text,
  source       text,
  covered_date date,
  issue_id     uuid references public.issues(id) on delete set null,
  newsletter   text
);

-- API tokens. One row per role. Values are generated by YOU (see step 6).
create table if not exists public.api_tokens (
  name  text primary key,       -- 'agent' | 'admin'
  token text not null
);

