-- 5. IMAGE STORAGE
-- ---------------------------------------------------------------------------
-- Public-read bucket, images only, 5 MB cap. The app serves resized/cropped
-- variants via Supabase's render/image transform (see site/lib/render-issue.js).
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values ('issue-images','issue-images', true, 5242880,
        array['image/png','image/jpeg','image/jpg','image/webp','image/gif'])
on conflict (id) do nothing;

drop policy if exists issue_images_anon_select on storage.objects;
create policy issue_images_anon_select on storage.objects
  for select using (bucket_id = 'issue-images');

-- KNOWN CONTAINED HOLE (see HANDOFF_HUMAN §"Before you go public"): this lets the
-- pipeline upload with the publishable/anon key. For production, replace this with
-- a service-role-only write (drop this policy, upload from a trusted server).
drop policy if exists issue_images_anon_insert on storage.objects;
create policy issue_images_anon_insert on storage.objects
  for insert with check (bucket_id = 'issue-images');

